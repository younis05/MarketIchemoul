    private void jbsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsaveActionPerformed
       String user=jname.getText();String pass=jpass.getText();  
       if(!verifyField()){
            adm=new Admin();
            adm.setUsername(user);
            adm.setPassword(pass);
            int status=AdminDB.save(adm);
            if(status>0){
                showTableAdmin();
                JOptionPane.showMessageDialog(null,"نجاح اخال المعلومات");
                clearTable();
            }else{
               JOptionPane.showMessageDialog(null,"خطأ إدخال البينات"); 
            }
       }else{
         JOptionPane.showMessageDialog(null,"أدخل البيانات...!");   
       }
       
    }//GEN-LAST:event_jbsaveActionPerformed

    private void jbdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbdeleteActionPerformed
       String iduser=jid.getText();
       if(!iduser.trim().isEmpty()){
       int msg=JOptionPane.showConfirmDialog(null, "هل تريد حذف المعلومات..!","حذف",JOptionPane.YES_NO_OPTION);
      if(msg==0){
        int id=Integer.parseInt(jid.getText());
        int status=AdminDB.delete(id);
            if(status>0){
                showTableAdmin();
                JOptionPane.showMessageDialog(null,"نجاح الحذف");
                clearTable();
            }else{
               JOptionPane.showMessageDialog(null,"خطأ إدخال البينات"); 
            }
        } }else{
         JOptionPane.showMessageDialog(null,"أدخل البيانات...!");   
       }
    }//GEN-LAST:event_jbdeleteActionPerformed

    private void jbupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbupdateActionPerformed
       String user=jname.getText();String pass=jpass.getText();
       String id=jid.getText();
        if(!id.trim().isEmpty()){
       if(!verifyField()){
            adm=new Admin();
            adm.setId(Long.parseLong(id));
            adm.setUsername(user);
            adm.setPassword(pass);
            int status=AdminDB.update(adm);
            if(status>0){
                showTableAdmin();
                JOptionPane.showMessageDialog(null,"نجاح التحديث");
                clearTable();
            }else{
               JOptionPane.showMessageDialog(null,"خطأ إدخال البينات"); 
            }
       }else{
         JOptionPane.showMessageDialog(null,"أدخل البيانات...!");   
       } }else{
         JOptionPane.showMessageDialog(null,"أدخل البيانات...!");   
       }
        
    }//GEN-LAST:event_jbupdateActionPerformed

