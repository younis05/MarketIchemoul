    private void showInfoAdmin(){
        int row=jtable.getSelectedRow();
        if(row==-1){
         JOptionPane.showMessageDialog(null,"إختر العمود المناسب");
        }else{
        String Table_Click=(jtable.getModel().getValueAt(row, 0)).toString();
        int id=Integer.parseInt(Table_Click);
        Admin adm=AdminDB.getAdmin(id);
        jid.setText(String.valueOf(adm.getId()));
        jname.setText(adm.getUsername());
        jpass.setText(adm.getPassword());  
        }
    }    
     
