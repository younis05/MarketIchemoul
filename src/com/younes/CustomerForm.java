    private void jbsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsaveActionPerformed
       String firstname=jfirstname.getText();String lastname=jlastname.getText();
       String phone=jphone.getText();String address=jaddress.getText();
       if(!verifyField()){
            cus=new Customer();
            cus.setFirstname(firstname);
            cus.setLastname(lastname);
            cus.setPhone(phone);
            cus.setAddress(address);
            int status=CustomerDB.save(cus);
            if(status>0){
                showTableCustomer();
                JOptionPane.showMessageDialog(null,"نجاح اخال المعلومات");
                clearTable();
            }else{
               JOptionPane.showMessageDialog(null,"خطأ إدخال البينات"); 
            }
       }else{
         JOptionPane.showMessageDialog(null,"أدخل البيانات...!");   
       }
       
    }//GEN-LAST:event_jbsaveActionPerformed

