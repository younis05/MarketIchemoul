    private void showInfoCustomer(){
        int row=jtable.getSelectedRow();
        if(row==-1){
         JOptionPane.showMessageDialog(null,"إختر العمود المناسب");
        }else{
        String Table_Click=(jtable.getModel().getValueAt(row, 0)).toString();
        int id=Integer.parseInt(Table_Click);
        Customer cus=CustomerDB.getCustomer(id);
        jid.setText(String.valueOf(cus.getCus_id()));
        jfirstname.setText(cus.getLastname());
        jlastname.setText(cus.getFirstname());
        jphone.setText(cus.getPhone());
        jaddress.setText(cus.getAddress());
        }
    }    
     
