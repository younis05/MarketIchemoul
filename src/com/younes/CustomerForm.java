    private void showTableCustomer(){
        
        try{
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("الرقم");
            model.addColumn("الإسم");
            model.addColumn("اللقب");
            model.addColumn("رفم  الهاتف");
            model.addColumn("العنوان");
            
            for(Customer cus:CustomerDB.getAllCustomer()){
                model.addRow(new Object[]{ cus.getCus_id(),cus.getFirstname(),
                cus.getLastname(),cus.getPhone(),cus.getAddress()});
            }
             jtable.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
	     jtable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
             jtable.setRowHeight(20);
             jtable.setModel(model);
             
             for(int i=0;i<5;i++){
                 jtable.getColumnModel().getColumn(i).setCellRenderer(tblcenter);
             }
        }catch(Exception ex){ex.printStackTrace();   }
    }
